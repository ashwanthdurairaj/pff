for(let i = 0; i < 5; i++)
{
    let stars = ''
    for(let j = 0; j <= i; j++)
    {
        stars += '*'
    }
    console.log(stars)
    console.log('')
}

for(let j = 5; j > 0; j--)
{
    let stars = ''
    for(let k = 0; k < j; k++)
    {
        stars += '*'
    }
    console.log(stars)
    console.log('')
}

console.log('Over')